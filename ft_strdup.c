#include "libft.h"

char	*ft_strdup(char *str)
{
	int		i;
	char	*p;

	i = 0;
	while (str[i])
		i++;
	p = malloc(i + 1);
	if (!p)
		return (NULL);
	i = 0;
	while (str[i])
	{
		p[i] = str[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
